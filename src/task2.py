# Лаб 1.2 — Метод прогонки
# Вариант 4
#
# -14*x1 -  6*x2                        = -78
#  -9*x1 + 15*x2 -    x3                = -73
#            x2  - 11*x3 +    x4        = -38
#                  -7*x3 + 12*x4 + 3*x5 =  77
#                           6*x4 -  7*x5 =  91

n = 5
a = [0, -9, 1, -7, 6]       # нижняя диагональ (a[0] не используется)
b = [-14, 15, -11, 12, -7]  # главная диагональ
c = [-6, -1, 1, 3, 0]       # верхняя диагональ (c[n-1] не используется)
d = [-78, -73, -38, 77, 91] # правая часть

print("=== Метод прогонки (вариант 4) ===\n")

# проверка устойчивости
print("Диагональное преобладание:")
for i in range(n):
    ai = abs(a[i]) if i > 0 else 0
    ci = abs(c[i]) if i < n-1 else 0
    ok = "+" if abs(b[i]) >= ai + ci else "-"
    print(f"  строка {i+1}: |{b[i]}|={abs(b[i])} vs {ai}+{ci}={ai+ci} [{ok}]")

# прямой ход
P = [0.0]*n
Q = [0.0]*n

P[0] = -c[0] / b[0]
Q[0] = d[0] / b[0]

for i in range(1, n):
    denom = b[i] + a[i]*P[i-1]
    P[i] = -c[i] / denom if i < n-1 else 0
    Q[i] = (d[i] - a[i]*Q[i-1]) / denom

print("\nПрогоночные коэффициенты:")
for i in range(n):
    print(f"  P[{i}] = {P[i]:.10f},  Q[{i}] = {Q[i]:.10f}")

# обратный ход
x = [0.0]*n
x[n-1] = Q[n-1]
for i in range(n-2, -1, -1):
    x[i] = P[i]*x[i+1] + Q[i]

print("\nРешение:")
for i in range(n):
    print(f"  x{i+1} = {x[i]:.10f}")

# проверка подстановкой
print("\nПроверка:")
for i in range(n):
    val = b[i]*x[i]
    if i > 0:
        val += a[i]*x[i-1]
    if i < n-1:
        val += c[i]*x[i+1]
    print(f"  ур.{i+1}: {val:.10f} (должно быть {d[i]}, невязка {val - d[i]:.2e})")
