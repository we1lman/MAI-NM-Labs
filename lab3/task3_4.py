# Лаб 3.4 — Численное дифференцирование
# Вариант 4
# X* = 0.2
# x_i = [0.0, 0.1, 0.2, 0.3, 0.4]
# y_i = [1.0, 1.1052, 1.2214, 1.3499, 1.4918]

def main():
    X = [0.0, 0.1, 0.2, 0.3, 0.4]
    Y = [1.0, 1.1052, 1.2214, 1.3499, 1.4918]
    x_star = 0.2
    n = len(X)

    print("=" * 65)
    print("  Лабораторная работа 3.4  |  Вариант 4")
    print("  Численное дифференцирование")
    print(f"  X* = {x_star}")
    print("=" * 65)

    print(f"\n  Исходные данные:")
    print(f"  {'i':>4}  {'x_i':>8}  {'y_i':>10}")
    print(f"  {'-'*24}")
    for i in range(n):
        print(f"  {i:>4}  {X[i]:>8.1f}  {Y[i]:>10.4f}")

    # Находим индекс i такой, что X* ∈ [x_i, x_{i+1}]
    # X* = 0.2 = x_2, значит используем отрезок [x_1, x_2] (левосторонняя)
    # и [x_2, x_3] (правосторонняя)
    # Для формулы (3.20) нужны x_i, x_{i+1}, x_{i+2} — берём i=1: x_1, x_2, x_3

    i = 1  # X* = x_2 — правая граница [x_1, x_2]

    # ── Первая производная: левосторонняя (формула 3.18, отрезок [x_1, x_2]) ──
    dy_left = (Y[i+1] - Y[i]) / (X[i+1] - X[i])
    print(f"\n  1. Первая производная (первый порядок точности):")
    print(f"     Левосторонняя (формула 3.18, отрезок [x_{i}, x_{i+1}]):")
    print(f"     y'({x_star}) ≈ (y_{i+1} - y_{i}) / (x_{i+1} - x_{i})")
    print(f"             = ({Y[i+1]} - {Y[i]}) / ({X[i+1]} - {X[i]})")
    print(f"             = {dy_left:.6f}")

    # ── Первая производная: правосторонняя (формула 3.18, отрезок [x_2, x_3]) ──
    dy_right = (Y[i+2] - Y[i+1]) / (X[i+2] - X[i+1])
    print(f"\n     Правосторонняя (формула 3.18, отрезок [x_{i+1}, x_{i+2}]):")
    print(f"     y'({x_star}) ≈ (y_{i+2} - y_{i+1}) / (x_{i+2} - x_{i+1})")
    print(f"             = ({Y[i+2]} - {Y[i+1]}) / ({X[i+2]} - {X[i+1]})")
    print(f"             = {dy_right:.6f}")

    # ── Первая производная: второй порядок точности (формула 3.20) ──
    # y'(x) ≈ (y_{i+1}-y_i)/(x_{i+1}-x_i) +
    #          [(y_{i+2}-y_{i+1})/(x_{i+2}-x_{i+1}) - (y_{i+1}-y_i)/(x_{i+1}-x_i)] / (x_{i+2}-x_i)
    #          × (2x - x_i - x_{i+1})
    diff1 = (Y[i+1] - Y[i]) / (X[i+1] - X[i])
    diff2 = (Y[i+2] - Y[i+1]) / (X[i+2] - X[i+1])
    second_diff = (diff2 - diff1) / (X[i+2] - X[i])
    dy_second = diff1 + second_diff * (2 * x_star - X[i] - X[i + 1])

    print(f"\n  2. Первая производная (второй порядок точности, формула 3.20):")
    print(f"     (y_{i+1}-y_{i})/(x_{i+1}-x_{i}) = {diff1:.6f}")
    print(f"     (y_{i+2}-y_{i+1})/(x_{i+2}-x_{i+1}) = {diff2:.6f}")
    print(f"     Вторая разделённая разность = ({diff2:.6f} - {diff1:.6f}) / ({X[i+2]} - {X[i]}) = {second_diff:.6f}")
    print(f"     y'({x_star}) ≈ {diff1:.6f} + {second_diff:.6f}·(2·{x_star} - {X[i]} - {X[i+1]})")
    print(f"             = {diff1:.6f} + {second_diff:.6f}·{2*x_star - X[i] - X[i+1]:.1f}")
    print(f"             = {dy_second:.6f}")

    print(f"\n     Замечание: для равномерной сетки формула (3.20) совпадает")
    print(f"     с полусуммой левосторонней и правосторонней производных:")
    print(f"     ({dy_left:.6f} + {dy_right:.6f}) / 2 = {(dy_left + dy_right) / 2:.6f}")

    # ── Вторая производная (формула 3.21) ──────────────────────────
    # y''(x) ≈ 2 · [(y_{i+2}-y_{i+1})/(x_{i+2}-x_{i+1}) - (y_{i+1}-y_i)/(x_{i+1}-x_i)] / (x_{i+2}-x_i)
    d2y = 2 * (diff2 - diff1) / (X[i+2] - X[i])

    print(f"\n  3. Вторая производная (формула 3.21):")
    print(f"     y''({x_star}) ≈ 2·[{diff2:.6f} - {diff1:.6f}] / ({X[i+2]} - {X[i]})")
    print(f"              = 2·{diff2 - diff1:.6f} / {X[i+2] - X[i]:.1f}")
    print(f"              = {d2y:.6f}")

    # ── Сводная таблица ────────────────────────────────────────────
    print(f"\n  Сводная таблица результатов в точке X* = {x_star}:")
    print(f"  {'-'*55}")
    print(f"  {'Величина':>35}  {'Значение':>14}")
    print(f"  {'-'*55}")
    print(f"  {'y`(X*) левосторонняя (1-й пор.)':>35}  {dy_left:>14.6f}")
    print(f"  {'y`(X*) правосторонняя (1-й пор.)':>35}  {dy_right:>14.6f}")
    print(f"  {'y`(X*) (2-й порядок точности)':>35}  {dy_second:>14.6f}")
    print(f"  {'y``(X*)':>35}  {d2y:>14.6f}")
    print(f"  {'-'*55}")


if __name__ == "__main__":
    main()
