# Лаб 1.2 — Метод прогонки (правая прогонка)
# Вариант 4
#
# Трёхдиагональная СЛАУ (обозначения по методичке §1.1.2):
#   a_i · x_{i-1} + b_i · x_i + c_i · x_{i+1} = d_i,   i = 1..n
#   a_1 = 0,  c_n = 0
#
# Система вар. 4:
#   -14·x1 -  6·x2                           = -78
#    -9·x1 + 15·x2 -    x3                   = -73
#              x2  - 11·x3 +    x4           = -38
#                    -7·x3 + 12·x4 + 3·x5   =  77
#                             6·x4 -  7·x5  =  91

n = 5
a = [0,  -9,  1, -7,  6]       # нижняя диагональ (a[0] = a_1 = 0)
b = [-14, 15, -11, 12, -7]     # главная диагональ
c = [-6,  -1,  1,  3,  0]      # верхняя диагональ (c[n-1] = c_n = 0)
d = [-78, -73, -38, 77, 91]    # правые части

print("=== Метод прогонки (вариант 4) ===\n")

# -------------------------------------------------------
# Проверка условия устойчивости (1.8):
#   |b_i| >= |a_i| + |c_i|  для всех i,
#   причём строгое неравенство хотя бы при одном i
# -------------------------------------------------------
print("Условие диагонального преобладания (1.8):")
for i in range(n):
    ai = abs(a[i]) if i > 0 else 0
    ci = abs(c[i]) if i < n-1 else 0
    ok = "+" if abs(b[i]) >= ai + ci else "-"
    print(f"  i={i+1}: |{b[i]}| = {abs(b[i])}  >=  |{a[i]}|+|{c[i]}| = {ai+ci}  [{ok}]")

# -------------------------------------------------------
# Прямой ход — вычисление прогоночных коэффициентов P_i, Q_i
#
# Формулы (1.5) для i = 1 (a_1 = 0):
#   P_1 = -c_1 / b_1
#   Q_1 =  d_1 / b_1
#
# Формулы (1.4) для i = 2..n-1:
#   P_i = -c_i / (b_i + a_i · P_{i-1})
#   Q_i = (d_i - a_i · Q_{i-1}) / (b_i + a_i · P_{i-1})
#
# Формулы (1.6) для i = n (c_n = 0):
#   P_n = 0
#   Q_n = (d_n - a_n · Q_{n-1}) / (b_n + a_n · P_{n-1})
# -------------------------------------------------------
P = [0.0]*n
Q = [0.0]*n

P[0] = -c[0] / b[0]
Q[0] =  d[0] / b[0]

for i in range(1, n):
    denom = b[i] + a[i]*P[i-1]
    P[i] = -c[i] / denom if i < n-1 else 0.0    # c_n = 0 → P_n = 0
    Q[i] = (d[i] - a[i]*Q[i-1]) / denom

print("\nПрогоночные коэффициенты:")
for i in range(n):
    print(f"  P[{i+1}] = {P[i]:.10f},  Q[{i+1}] = {Q[i]:.10f}")

# -------------------------------------------------------
# Обратный ход — формула (1.7):
#   x_n = Q_n  (т.к. P_n = 0)
#   x_i = P_i · x_{i+1} + Q_i,   i = n-1..1
# -------------------------------------------------------
x = [0.0]*n
x[n-1] = Q[n-1]
for i in range(n-2, -1, -1):
    x[i] = P[i]*x[i+1] + Q[i]

print("\nРешение:")
for i in range(n):
    print(f"  x{i+1} = {x[i]:.10f}")

# -------------------------------------------------------
# Проверка подстановкой
# -------------------------------------------------------
print("\nПроверка (подстановка в исходную систему):")
for i in range(n):
    val = b[i]*x[i]
    if i > 0:
        val += a[i]*x[i-1]
    if i < n-1:
        val += c[i]*x[i+1]
    print(f"  ур.{i+1}: {val:.10f}  (должно быть {d[i]},  невязка {val - d[i]:.2e})")
